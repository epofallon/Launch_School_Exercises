# Enumerable#each_cons
# - iterates over the members of a collection taking each sequence of `n`
#   consecutive elements at a time and passing them to the associated block
#   for processing.
# - returns a value of `nil`

# write a method that:
# - takes an array 2 elements at a time
# - yeilds to a block
require 'pry'
def each_cons(arr, num)
  arr[0..-num].each_with_index { |item, idx| yield(*arr[idx, num]) }
  nil
end

hash = {}
each_cons([1, 3, 6, 10], 1) do |value|
  hash[value] = true
end
p hash == { 1 => true, 3 => true, 6 => true, 10 => true }

hash = {}
each_cons([1, 3, 6, 10], 2) do |value1, value2|
  hash[value1] = value2
end
p hash == { 1 => 3, 3 => 6, 6 => 10 }

hash = {}
each_cons([1, 3, 6, 10], 3) do |value1, *values|
  hash[value1] = values
end
p hash == { 1 => [3, 6], 3 => [6, 10] }

hash = {}
each_cons([1, 3, 6, 10], 4) do |value1, *values|
  hash[value1] = values
end
p hash == { 1 => [3, 6, 10] }

hash = {}
each_cons([1, 3, 6, 10], 5) do |value1, *values|
  hash[value1] = values
end
p hash == {}