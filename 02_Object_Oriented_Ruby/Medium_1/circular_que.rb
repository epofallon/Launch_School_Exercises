class CircularQueue
  def initialize(que_size)
    @size = que_size
    @arr = []
  end

  def enqueue(obj) # add an object to the queue
    dequeue if @arr.size == @size
    @arr << obj
  end

  def dequeue # remove and return the oldest object in the queue (`nil` if the queue is empty)
    @arr.shift
  end
end

queue = CircularQueue.new(3)
puts queue.dequeue == nil

queue.enqueue(1)
